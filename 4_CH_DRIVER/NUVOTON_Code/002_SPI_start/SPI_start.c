
#include <stdio.h>
#include "NuMicro.h"
#include "Init.h"
//-----------------------------------------------------------------------------------//
void SYS_Init(void);
void SPI_Init(void);
//-----------------------------------------------------------------------------------//
int main(void)
{	  
  SYS_UnlockReg();
  SYS_Init();    
  SYS_LockReg();
  SPI_Init();
//-----------------------------------------------------------------------------------//
  while(1)
  { 
  SPI_WRITE_TX(SPI0, 0xEE);
  while(SPI_IS_BUSY(SPI0));
  TIMER_Delay(TIMER1, 10);         
  }
}
